SELECT Name
FROM EMPLOYEE
WHERE Id in (
    SELECT ManagerID
    FROM EMPLOYEE
    WHERE ManagerID IS NOT NULL
    GROUP BY ManagerId
    HAVING COUNT(ManagerID) >= 5
);

